#include "header.h"

void	ft_change(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\n')
	{
		i++;
	}
	i += 1;
	while (str[i])
	{
		while (str[i] == 'o')
		{
			write(1, "j", 1);
			i++;
		}
		while (str[i] == '\n')
		{
			write(1, "\n", 1);
			i++;
		}
		write(1, ".", 1);
		i++;
	}
}
